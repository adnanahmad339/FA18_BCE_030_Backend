const jwt = require("jsonwebtoken");


// VERIFYING TOKEN HERE. IF THE PERSON EXIST OR NOT.
const verifyToken = (req, res, next) => {
  const authHeader = req.headers.token;
  if (authHeader) {
    const token = authHeader.split(" ")[1];
    jwt.verify(token, process.env.JWT_SECRET, (err, user) => {
      if (err) res.status(403).json("Token is not valid!");
      req.user = user;
      next();
    });
  } else {
    return res.status(401).json("You are not authenticated!");
  }
};


// IF THE USER IS NOT ADMIN AND NOT A USER HE CAN'T DO THE WILLING TASK
const verifyTokenAndAuthorization = (req, res, next) => {
  verifyToken(req, res, () => {
    if (req.user.id === req.params.id || req.user.isAdmin) {
      next();
    } else {
      res.status(403).json("You are not allowed to do that!");
    }
  });
};





// VERIFYING IF THE PERSON EXIST AND IF YES THEN WHETHER HE IS ADMIN OR NOT
const verifyTokenAndAdmin = (req, res, next) => {
 
  verifyToken(req, res, () => {



    if (req.body.id=== req.body.isAdmin) {
      next();
    } else {
      res.status(403).json("You are not allowed to do that!");
    }
  });
};

module.exports = {
  verifyToken,
  verifyTokenAndAuthorization,
  verifyTokenAndAdmin,
};